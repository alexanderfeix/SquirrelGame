package hs.augsburg.squirrelgame.util;

public class Position {

    private double x;
    private double y;

    public Position(double posX, double posY){
        this.x = posX;
        this.y = posY;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
